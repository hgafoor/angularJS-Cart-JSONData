// product class
function product(id, name, desc, price, colors, type, category, photo) {
    this.id = id; // product code (id = stock keeping unit)
    this.name = name;
    this.description = description;
    this.price = price;
    this.colors = colors;
    this.type= type;
    this.category = category;
    this.photo = photo;
}
